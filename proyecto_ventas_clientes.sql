DROP TABLE IF EXISTS ventas;
DROP TABLE IF EXISTS clientes;

CREATE TABLE clientes (
    id INT PRIMARY KEY,
    nombre_cliente VARCHAR(100),
    ciudad VARCHAR(50)
);

CREATE TABLE ventas (
    id INT PRIMARY KEY,
    cliente_id INT,
    fecha DATE,
    total_venta INT,
    FOREIGN KEY (cliente_id) REFERENCES clientes(id)
);

